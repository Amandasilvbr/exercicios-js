function write(num) {
    switch (num) {
        case 1:
            alert("Um.")
            break
        case 2:
            alert("Dois.")
            break
        case 3:
            alert("Três.")
            break
        case 4:
            alert("Quatro.")
            break
        case 5:
            alert("Cinco.")
            break
        case 6:
            alert("Seis.")
            break
        case 7:
            alert("Sete.")
            break
        case 8:
            alert("Oito.")
            break
        case 9:
            alert("Nove.")
            break
        case 10:
            alert("Dez.")
            break
        default:
            return "Número fora do intervalo."
    }
}

write(2);